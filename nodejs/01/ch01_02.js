// 기본 자료형 
console.log(23 + 45);
console.log(23 - 45);
console.log(23 * 45);
console.log(23 / 45);
console.log(23 % 45);

console.log('안녕하세요');
console.log('안녕하세요 "홍길동님"'); // "를 출력하고 싶으면 '안에 감싸면 된다 
// console.log("안녕하세요 "홍길동님""); // 이렇게하면 syntax 에러가 발생함 
console.log("안녕하세요 \"홍길동님\""); // 앞에 슬래시 넣으면 "" 출력 가능 

console.log("안녕하세요 \n홍길동 입니다.");
console.log("안녕하세요\t홍길동입니다.");
console.log("안녕하세요 \\홍길동입니다.");
console.log("안녕하세요" + "홍길동입니다"); // 띄워쓰기 없이 concatenation
console.log(`52 + 45 = ${52 + 45}`); 

console.log( 52 > 52 );
console.log( 52 == 52 );
console.log( 52 != 52 );

console.log( 52 >= 52 && 52 > 52 );
console.log( 52 >= 52 || 52 > 52 );